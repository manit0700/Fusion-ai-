-- Create a new database for the dataset
CREATE DATABASE IF NOT EXISTS telecom_db;

USE telecom_db;

-- Create customer_info table
CREATE TABLE customer_info (
    customer_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    email VARCHAR(100),
    phone_number VARCHAR(100),
    date_of_birth DATE,
    address TEXT
);

-- Create service_plans table
CREATE TABLE service_plans (
    plan_id INT AUTO_INCREMENT PRIMARY KEY,
    plan_name VARCHAR(50),
    monthly_cost DECIMAL(5,2),
    data_limit_gb INT,
    voice_minutes INT,
    sms_limit INT
);

-- Create purchase_info table
CREATE TABLE purchase_info (
    purchase_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    plan_id INT,
    purchase_date DATE,
    amount_paid DECIMAL(7,2),
    payment_method VARCHAR(100),
    FOREIGN KEY (customer_id) REFERENCES customer_info(customer_id),
    FOREIGN KEY (plan_id) REFERENCES service_plans(plan_id)
);

-- Create call_data_records table
CREATE TABLE call_data_records (
    call_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    start_time TIMESTAMP,
    end_time TIMESTAMP,
    origin VARCHAR(100),
    destination VARCHAR(100),
    call_duration INT,
    call_cost DECIMAL(6,2),
    FOREIGN KEY (customer_id) REFERENCES customer_info(customer_id)
);



